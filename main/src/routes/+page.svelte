			<h2 class="kern-10 text-xl uppercase">Is working for <i>Kopis</i> a job or an adventure?</h2>
			<p>True or False? Working at Kopis is an adventure?</p>
			<br />
			<details>
				<summary>Reveal the answer!</summary>
				<p>
					False. It is a job, but here are a few things we do to make it a little more adventurous.
				</p>
				<br />
				<h2>Gumballs</h2>
				<p>
					Remember those "ilities" we were talking about earlier? Yeah, we actually printed them on
					gumballs! When we are taking on a new project we get around the table and Andy gives us a
					stack of quarters. If the project is large and the client is willing, he give us a lot of
					quarters. We take the first quarter to the gumball machine, drop in the quarter and see
					what comes out! Occasionally it makes no sense, but generally there is applicability
					(yeah, I know). Once, while working on a church landing page, we got "resurrectablility".
					We all laughed and then we got to work. We were able to resurrect the church's website
					with the help of a few more gumballs.
					<br />
					Why do we do it this way instead of going through an organized list? Because it is fun and
					it gets us talking about the project. We get to know each other better and we get to know the
					project better. It is a great way to start a project. A more systematic approach soon follows,
					but the gumball approach gives us all something to chew on (yup).
				</p>
				<h2>Puzzles</h2>
				<p>
					We believe in "ilites" so much that we have created hexagon-shaped puzzle pieces that we
					assemble after a project has been completed to memorialize the architectural blueprint of
					that solution.
				</p>
				<br />
				<h2>Pranks</h2>
				<p>
					You literally would not believe the lengths to which some of our employees go to ensure
					you get your fair share of humility. I'll simply direct you to <u
						><a href="https://kopisusa.com/art-good-office-prank/">The Art of a Good Office Prank</a
						>.</u
					>
				</p>
				<br />
				<h2>Pair Programming</h2>
				<p>
					Let's face it, we are pretty good at cranking out code from the flow-zone but sometimes we
					get stuck. Some problems are hard and rubber duck debugging is not enough. When that
					happens, we help each other get better. One way we do this by pairing up and working
					through the problem together. This is one aspect of <u>respect</u> and a way we grow both professionally
					and as human beings.
				</p>
			</details>
